# Crear un directorio para guardar las imágenes si no existe
output_dir = "number_images"
if not os.path.exists(output_dir):
    os.makedirs(output_dir)

# Función para crear y guardar una imagen a partir de una fila
def create_image_from_row(row, index):
    image = row.reshape(7, 5)  # Reshape para crear una imagen 7x5
    plt.imshow(image, cmap="gray_r", interpolation="nearest")  # Usar "gray_r" para que 1 sea negro y 0 sea blanco
    
    plt.axis('off')  # Quitar los ejes
    plt.savefig(os.path.join(output_dir, f"numero_{index}.png"))  # Guardar la imagen
    plt.close()

# Generar las imágenes para cada fila
for idx, row in enumerate(data):
    create_image_from_row(row, idx)
